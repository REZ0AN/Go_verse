package main

import "fmt"

func main() {
	/**
	* Comments
	multiple lines

	*/
	// Single line comment
	fmt.Println("Hello, World!")
	// escape sequence
	// newline
	fmt.Println("Hello, \nWorld!")
	// tab
	fmt.Println("Hello, \tWorld!")
	// backspace
	fmt.Println("Hello, \bWorld!")
	// carriage return
	fmt.Println("Hello, \rWorld!")
	// form feed
	fmt.Println("Hello, \fWorld!")
	// single quote
	fmt.Println("Hello, 'World!")
	// double quote
	fmt.Println("Hello, \"World!")
	// backslash
	fmt.Println("Hello, \\World!")
	// percent
	fmt.Println("Hello, %World!")
	// octal
	fmt.Println("Hello, \141World!")
	// hexadecimal
	fmt.Println("Hello, \x61World!")
	// unicode
	fmt.Println("Hello, \u0061World!")
	// unicode
	fmt.Println("Hello, \U00000061World!")

}
